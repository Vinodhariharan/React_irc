//Program to print a greeting with the given name

function greet(name) {
    return "Hello, " + name + "!";
  }
  
  const add = function(a, b) {
    return a + b;
  };
  
  const multiply = (a, b) => a * b;
  
  console.log(greet("Vinod"));      // Output: Hello, Vinod!
  console.log(add(3, 5));             // Output: 8
  console.log(multiply(2, 4));        // Output: 8